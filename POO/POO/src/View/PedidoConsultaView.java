    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (codigoPedido.getText().equals("") || codigoPedido.getText().matches("[0-9]+") == false) {
            JOptionPane.showMessageDialog(null, "Codigo invalido!");
        }
        int convert = 0;
        PedidoController pedidoC = new PedidoController();
        ArrayList<PedidosModel> arrayP = new ArrayList<>();
        arrayP = pedidoC.GetPedidos();
        convert = Integer.parseInt(codigoPedido.getText());

        for (int i = 0; i < arrayP.size(); i++) {
            if (convert == arrayP.get(i).getCodigo()) {
                DefaultTableModel tabela = (DefaultTableModel) tabelaP.getModel();
                tabela.setRowCount(0);
                Object[] dados = {arrayP.get(i).getCodigo(), arrayP.get(i).getStatus(),
                    arrayP.get(i).getFormaPagamento(), arrayP.get(i).getTotal(),
                    arrayP.get(i).getCpf(), arrayP.get(i).getPlaca()};
                tabela.addRow(dados);
                break;
            }
        }
        if (convert == 0) {
            JOptionPane.showMessageDialog(null, "Codigo invalido!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

