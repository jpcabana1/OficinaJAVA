    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        PedidoController pedidoC = new PedidoController();
        ArrayList<PedidosModel> arrayP = new ArrayList<>();
        arrayP = pedidoC.GetPedidos();
        RelatorioController relatorioC = new RelatorioController();
        if (reason.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, digite o que achou da nossa oficina");
            return;
        }
        for (int i = 0; i < arrayP.size(); i++) {
            if (placaCarro.getText().compareToIgnoreCase(arrayP.get(i).getPlaca()) == 0) {
                System.out.println("ENTROU PRO RELATORIO");
                if (arrayP.get(i).getStatus().compareToIgnoreCase("Finalizado") == 0
                        || arrayP.get(i).getStatus().compareToIgnoreCase("Cancelado") == 0) {
                    RelatorioVendasModel rv = new RelatorioVendasModel(arrayP.get(i).getTotal(), arrayP.get(i).getStatus(), reason.getText());
                    relatorioC.CadRelatorioDAO(rv);
                    JOptionPane.showMessageDialog(null, "Obrigado por vir em nossa Oficina, volte sempre!.");
                    pedidoC.removerPedido(placaCarro.getText());
                    init();
                    return;
                } else {
                    JOptionPane.showMessageDialog(null, "Pedido ainda não foi finalizado.");
                    return;
                }
            }
        }
        JOptionPane.showMessageDialog(null, "Nao existe pedido para essa placa");
    }//GEN-LAST:event_jButton2ActionPerformed

