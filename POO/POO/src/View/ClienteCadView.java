    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int qtdCpf = cpfCliente.getText().length();
        int qtdRg = rgCliente.getText().length();
        int x = 0;

        if (nomeCliente.getText().equals("") || cpfCliente.getText().equals("")
                || rgCliente.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else if (nomeCliente.getText().matches("[0-9]+") == true) {
            JOptionPane.showMessageDialog(null, "Nome invalido!");
        } else if (rgCliente.getText().matches("[0-9]+") == false || qtdRg != 7) {
            JOptionPane.showMessageDialog(null, "RG invalido");
        } else if (cpfCliente.getText().matches("[0-9]+") == false || qtdCpf != 11) {
            JOptionPane.showMessageDialog(null, "CPF invalido");
        } else {
            ArrayList<ClienteModel> arrayC = new ArrayList();
            ClienteController controllerC = new ClienteController();
            arrayC = controllerC.getCliente();

            for (int i = 0; i < arrayC.size(); i++) {
                if (arrayC.get(i).getCpf().compareToIgnoreCase(cpfCliente.getText()) == 0
                        || arrayC.get(i).getRg().compareToIgnoreCase(rgCliente.getText()) == 0) {
                    x = 1;
                    JOptionPane.showMessageDialog(null, "CPF e/ou RG jÃ¡ existe!");
                }
            }
            if (x == 0){
            ClienteModel cliente = new ClienteModel(nomeCliente.getText(), cpfCliente.getText(), rgCliente.getText());
            ClienteController clienteC = new ClienteController();
            clienteC.cadClienteDAO(cliente);
            JOptionPane.showMessageDialog(null, "Cliente Cadastrado com sucesso!");
            cadastroCarro = true;
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

